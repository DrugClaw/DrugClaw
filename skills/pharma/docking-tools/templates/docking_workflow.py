    def best_docking_rows(self, summary_rows: list[dict[str, Any]]) -> list[dict[str, Any]]:
        best: dict[str, dict[str, Any]] = {}
        for row in summary_rows:
            score = row.get("score_kcal_mol")
            if score is None:
                continue
            slug = row["ligand_slug"]
            ligand_state = self.session["ligands"].get(slug, {})
            current = best.get(slug)
            if current is None or float(score) < float(current["best_score_kcal_mol"]):
                best[slug] = {
                    "id": slug,
                    "ligand": row["ligand"],
                    "best_receptor": row["receptor"],
                    "best_receptor_slug": row["receptor_slug"],
                    "best_score_kcal_mol": float(score),
                    "complex_path": row.get("complex_path", ""),
                    "smiles": str(ligand_state.get("smiles") or ""),
                    "properties_path": str(ligand_state.get("properties_path") or ""),
                }
        return sorted(best.values(), key=lambda item: item["best_score_kcal_mol"])

