    def parse_vina_score(self, output_path: Path, log_path: Path) -> Optional[float]:
        if output_path.exists():
            for line in output_path.read_text(encoding="utf-8", errors="ignore").splitlines():
                if line.startswith("REMARK VINA RESULT:"):
                    match = re.search(r"(-?\d+\.\d+)", line)
                    if match:
                        return float(match.group(1))
        if log_path.exists():
            for line in log_path.read_text(encoding="utf-8", errors="ignore").splitlines():
                if re.match(r"\s*1\s+-?\d+\.\d+", line):
                    match = re.search(r"(-?\d+\.\d+)", line)
                    if match:
                        return float(match.group(1))
        return None

    def dock_pair(self, receptor_slug: str, ligand_slug: str) -> DockingRecord:
        receptor = ReceptorRecord(**self.session["receptors"][receptor_slug])
        ligand = LigandRecord(**self.session["ligands"][ligand_slug])
        if not receptor.box:
            self.generate_boxes()
            receptor = ReceptorRecord(**self.session["receptors"][receptor_slug])
        box = BoxParams(**receptor.box)
        config_path = self.write_pair_config(receptor, ligand, box)
        output_path = self.workspace.docking_output_path(receptor_slug, ligand_slug)
        log_path = self.workspace.docking_log_path(receptor_slug, ligand_slug)
        vina = self.tool("vina_bin")
        run_command([vina, "--config", str(config_path), "--log", str(log_path)], timeout=7200)
        score = self.parse_vina_score(output_path, log_path)
        record = DockingRecord(
            receptor=receptor_slug,
            ligand=ligand_slug,
            config_path=str(config_path),
            output_path=str(output_path),
            log_path=str(log_path),
            score_kcal_mol=score,
        )
        log(f"Docked {receptor.name} vs {ligand.name}: score={score}")
        return record

    def run_docking(self) -> list[DockingRecord]:
        if not self.session["receptors"] or any(not data.get("prepared_pdbqt_path") for data in self.session["receptors"].values()):
            self.prepare_receptors()
        if not self.session["ligands"] or any(not data.get("prepared_pdbqt_path") for data in self.session["ligands"].values()):
            self.prepare_ligands()
        if any(not data.get("box") for data in self.session["receptors"].values()):
            self.generate_boxes()
        pairs = self.selected_pairs()
        concurrency = max(1, int(self.settings.get("concurrency", 4)))
        results: list[DockingRecord] = []
        with ThreadPoolExecutor(max_workers=concurrency) as pool:
            futures = {pool.submit(self.dock_pair, receptor, ligand): (receptor, ligand) for receptor, ligand in pairs}
            for future in as_completed(futures):
                receptor, ligand = futures[future]
                try:
                    results.append(future.result())
                except Exception as exc:
                    log(f"Docking failed for {receptor} vs {ligand}: {exc}", "ERROR")
                    results.append(
                        DockingRecord(
                            receptor=receptor,
                            ligand=ligand,
                            config_path=str(self.workspace.docking_pair_config_path(receptor, ligand)),
                            output_path=str(self.workspace.docking_output_path(receptor, ligand)),
                            log_path=str(self.workspace.docking_log_path(receptor, ligand)),
                            score_kcal_mol=None,
                        )
                    )
        results.sort(key=lambda item: (item.receptor, item.ligand))
        self.session["docking"] = [asdict(item) for item in results]
        self.append_history("run_docking", "success", {"count": len(results)})
        self.save_session()
        return results

    def first_model_coords(self, pdbqt_path: Path) -> list[tuple[float, float, float]]:
        coords: list[tuple[float, float, float]] = []
        in_model = False
        with pdbqt_path.open("r", encoding="utf-8", errors="ignore") as handle:
            for line in handle:
                if line.startswith("MODEL"):
                    in_model = True
                    continue
                if line.startswith("ENDMDL"):
                    break
                if (not in_model and line.startswith(("ATOM", "HETATM"))) or (in_model and line.startswith(("ATOM", "HETATM"))):
                    try:
                        coords.append(
                            (
                                float(line[30:38].strip()),
                                float(line[38:46].strip()),
                                float(line[46:54].strip()),
                            )
                        )
                    except Exception:
                        continue
        return coords

    def combine_complex(self, docking_record: DockingRecord) -> DockingRecord:
        receptor = ReceptorRecord(**self.session["receptors"][docking_record.receptor])
        ligand = LigandRecord(**self.session["ligands"][docking_record.ligand])
        output_pdb = self.workspace.complex_path(receptor.slug, ligand.slug)
        receptor_pdb = Path(receptor.prepared_pdb_path)
        if not receptor_pdb.exists():
            obabel = self.tool("obabel_bin")
            run_command([obabel, receptor.prepared_pdbqt_path, "-O", str(output_pdb.with_suffix(".receptor_tmp.pdb")), "-opdb", "-d"], timeout=1200)
            receptor_pdb = output_pdb.with_suffix(".receptor_tmp.pdb")
        ligand_pdbqt = Path(docking_record.output_path)
        with output_pdb.open("w", encoding="utf-8") as handle:
            atom_count = 0
            for line in receptor_pdb.read_text(encoding="utf-8", errors="ignore").splitlines():
                if line.startswith(("ATOM", "HETATM")):
                    atom_count += 1
                    handle.write(line[:6] + f"{atom_count:>5}" + line[11:] + "\n")
            handle.write(f"TER   {atom_count:>5}\n")
            ligand_lines = ligand_pdbqt.read_text(encoding="utf-8", errors="ignore").splitlines()
            ligand_written = 0
            in_model = False
            for line in ligand_lines:
                if line.startswith("MODEL"):
                    in_model = True
                    continue
                if line.startswith("ENDMDL"):
                    break
                if line.startswith(("ATOM", "HETATM")) and (in_model or not any(x.startswith("MODEL") for x in ligand_lines)):
                    atom_count += 1
                    ligand_written += 1
                    atom_name = (line[12:16].strip() or "C")[:4]
                    res_name = slugify(ligand.name).upper()[:3].ljust(3)
                    element = (line[77:78].strip() or atom_name[0]).upper()
                    new_line = (
                        f"HETATM{atom_count:>5} {atom_name:<4} {res_name} L{1:>4}    "
                        f"{line[30:38]}{line[38:46]}{line[46:54]}  1.00  0.00           {element:>2}"
                    )
                    handle.write(new_line + "\n")
            handle.write(f"TER   {atom_count:>5}\nEND\n")
        if receptor_pdb.name.endswith(".receptor_tmp.pdb") and receptor_pdb.exists():
            receptor_pdb.unlink()
        docking_record.complex_path = str(output_pdb)
        docking_record.conformation_ok = self.validate_conformation(Path(docking_record.output_path), output_pdb)
        log(f"Combined complex {output_pdb.name}: conformation_ok={docking_record.conformation_ok}")
        return docking_record

    def validate_conformation(self, original_pdbqt: Path, converted_pdb: Path) -> bool:
        original = self.first_model_coords(original_pdbqt)
        converted: list[tuple[float, float, float]] = []
        with converted_pdb.open("r", encoding="utf-8", errors="ignore") as handle:
            in_ligand = False
            for line in handle:
                if line.startswith("HETATM") and " L   1" in line:
                    in_ligand = True
                    try:
                        converted.append(
                            (
                                float(line[30:38].strip()),
                                float(line[38:46].strip()),
                                float(line[46:54].strip()),
                            )
                        )
                    except Exception:
                        continue
                elif in_ligand and line.startswith("TER"):
                    break
        if len(original) != len(converted) or not original:
            return False
        max_dev = 0.0
        for left, right in zip(original, converted):
            dev = math.sqrt(sum((a - b) ** 2 for a, b in zip(left, right)))
            max_dev = max(max_dev, dev)
        return max_dev < 0.01

    def ligand_atoms_outside_box(self, docking_record: DockingRecord) -> int:
        receptor = ReceptorRecord(**self.session["receptors"][docking_record.receptor])
        if not receptor.box or not docking_record.complex_path:
            return 0
        box = BoxParams(**receptor.box)
        half = [box.size_x / 2.0, box.size_y / 2.0, box.size_z / 2.0]
        outside = 0
        with Path(docking_record.complex_path).open("r", encoding="utf-8", errors="ignore") as handle:
            for line in handle:
                if line.startswith("HETATM") and " L   1" in line:
                    x = float(line[30:38].strip())
                    y = float(line[38:46].strip())
                    z = float(line[46:54].strip())
                    if (
                        x < box.center_x - half[0]
                        or x > box.center_x + half[0]
                        or y < box.center_y - half[1]
                        or y > box.center_y + half[1]
                        or z < box.center_z - half[2]
                        or z > box.center_z + half[2]
                    ):
                        outside += 1
        return outside

    def render_top_hits(self, top_n: Optional[int] = None) -> list[DockingRecord]:
        if not self.session["docking"]:
            self.run_docking()
        top_n = top_n or int(self.settings.get("render_top_n", 3))
        pymol = which_or_override("pymol", self.settings.get("pymol_bin"))
        records = [DockingRecord(**item) for item in self.session["docking"] if item.get("score_kcal_mol") is not None]
        records.sort(key=lambda item: item.score_kcal_mol if item.score_kcal_mol is not None else 999.0)
        selected = records[:top_n]
        for record in selected:
            record = self.combine_complex(record)
            receptor = ReceptorRecord(**self.session["receptors"][record.receptor])
            ligand = LigandRecord(**self.session["ligands"][record.ligand])
            pml_path = self.workspace.render_script_path(receptor.slug, ligand.slug)
            png_path = self.workspace.render_png_path(receptor.slug, ligand.slug)
            script = textwrap.dedent(
                f"""\
                load {record.complex_path}, complex
                split_states complex
                hide everything
                show cartoon, complex and polymer
                show sticks, resn {slugify(ligand.name).upper()[:3]}
                color slate, complex and polymer
                color tv_orange, resn {slugify(ligand.name).upper()[:3]}
                select pocket, byres (complex and polymer within 5 of resn {slugify(ligand.name).upper()[:3]})
                show sticks, pocket
                color cyan, pocket
                bg_color white
                png {png_path}, width=1800, height=1350, dpi=200, ray=1
                quit
                """
            )
            write_text(pml_path, script)
            record.render_script_path = str(pml_path)
            record.render_path = str(png_path)
            if pymol:
                try:
                    run_command([pymol, "-cq", str(pml_path)], timeout=1800)
                except Exception as exc:
                    log(f"PyMOL render failed for {pml_path.name}: {exc}", "WARNING")
            self._replace_docking_record(record)
        self.append_history("render_top_hits", "success", {"count": len(selected)})
        self.save_session()
        return selected

    def _replace_docking_record(self, updated: DockingRecord) -> None:
        records = [DockingRecord(**item) for item in self.session["docking"]]
        replaced = False
        for index, record in enumerate(records):
            if record.receptor == updated.receptor and record.ligand == updated.ligand:
                records[index] = updated
                replaced = True
                break
        if not replaced:
            records.append(updated)
        self.session["docking"] = [asdict(item) for item in records]

