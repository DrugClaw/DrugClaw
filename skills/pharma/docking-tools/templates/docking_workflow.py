    def detect_receptor_type(self, pdb_path: Path) -> str:
        protein_hits = 0
        residue_types: set[str] = set()
        atom_count = 0
        with pdb_path.open("r", encoding="utf-8", errors="ignore") as handle:
            for line in handle:
                if line.startswith(("ATOM", "HETATM")):
                    atom_count += 1
                    residue = line[17:20].strip().upper()
                    residue_types.add(residue)
                    if residue in set(AA_MAP.values()):
                        protein_hits += 1
        if protein_hits > 80 or atom_count > 500:
            return "protein"
        if residue_types & set(AA_MAP.values()):
            return "peptide"
        if residue_types & {"A", "C", "G", "T", "U", "DA", "DC", "DG", "DT", "DU"}:
            return "nucleic"
        return "small_molecule"

    def normalize_receptor_to_pdb(self, raw_path: Path, slug: str) -> Path:
        receptor_dir = self.workspace.receptor_dir(slug)
        normalized = receptor_dir / f"{slug}.pdb"
        if raw_path.suffix.lower() == ".pdb":
            copy_file(raw_path, normalized)
            return normalized
        obabel = self.tool("obabel_bin")
        run_command([obabel, str(raw_path), "-O", str(normalized), "-opdb"], timeout=600)
        return normalized

    def prepare_receptors(self) -> dict[str, ReceptorRecord]:
        if not self.session["receptors"]:
            self.fetch_receptors()
        prepared: dict[str, ReceptorRecord] = {}
        obabel = self.tool("obabel_bin")
        pdbfixer = self.tool("pdbfixer_bin")
        ph = str(self.settings.get("ph", 7.4))
        for slug, record_data in self.session["receptors"].items():
            record = ReceptorRecord(**record_data)
            raw_path = Path(record.raw_path)
            receptor_dir = self.workspace.receptor_dir(slug)
            normalized_pdb = self.normalize_receptor_to_pdb(raw_path, slug)
            receptor_type = self.detect_receptor_type(normalized_pdb)
            output_pdbqt = receptor_dir / f"{slug}.pdbqt"
            output_pdb = receptor_dir / f"{slug}.pdb"
            if receptor_type == "protein":
                temp_clean = receptor_dir / f"{slug}_clean.pdb"
                temp_fixed = receptor_dir / f"{slug}_fixed.pdb"
                run_command([pdbfixer, str(normalized_pdb), "--output", str(temp_clean), "--keep-heterogens=none"], timeout=1200)
                run_command([
                    pdbfixer,
                    str(temp_clean),
                    "--output",
                    str(temp_fixed),
                    "--add-residues",
                    "--add-atoms",
                    "all",
                    "--ph",
                    ph,
                ], timeout=1200)
                copy_file(temp_fixed, output_pdb)
                run_command([
                    obabel,
                    str(temp_fixed),
                    "-O",
                    str(output_pdbqt),
                    "-xr",
                    "--addindex",
                    "--partialcharge",
                    "gasteiger",
                ], timeout=1200)
                for temp_path in [temp_clean, temp_fixed]:
                    if temp_path.exists():
                        temp_path.unlink()
            else:
                copy_file(normalized_pdb, output_pdb)
                run_command([
                    obabel,
                    str(normalized_pdb),
                    "-O",
                    str(output_pdbqt),
                    "--partialcharge",
                    "gasteiger",
                ], timeout=1200)
            record.receptor_type = receptor_type
            record.prepared_pdb_path = str(output_pdb)
            record.prepared_pdbqt_path = str(output_pdbqt)
            record.notes.append(f"prepared as {receptor_type}")
            self.session["receptors"][slug] = asdict(record)
            prepared[slug] = record
            log(f"Prepared receptor {record.name}: {output_pdbqt}")
        self.append_history("prepare_receptors", "success", {"count": len(prepared)})
        self.save_session()
        return prepared

    def is_2d_structure(self, file_path: Path) -> bool:
        content = file_path.read_text(encoding="utf-8", errors="ignore")
        lower = file_path.suffix.lower()
        if lower in {".sdf", ".mol"}:
            lines = content.splitlines()
            if len(lines) >= 5:
                try:
                    atom_count = int(lines[3][0:3].strip())
                except Exception:
                    return True
                for line in lines[4 : 4 + atom_count]:
                    if len(line) >= 40:
                        try:
                            if abs(float(line[30:40].strip())) > 0.1:
                                return False
                        except Exception:
                            continue
                return True
        if lower in {".pdb", ".pdbqt"}:
            for line in content.splitlines():
                if line.startswith(("ATOM", "HETATM")):
                    try:
                        if abs(float(line[46:54].strip())) > 0.1:
                            return False
                    except Exception:
                        continue
            return True
        return False

    def validate_pdbqt(self, file_path: Path) -> bool:
        if not file_path.exists() or file_path.stat().st_size == 0:
            return False
        content = file_path.read_text(encoding="utf-8", errors="ignore")
        return "ATOM" in content or "HETATM" in content

    def detect_biomolecule(self, file_path: Path) -> str:
        if file_path.suffix.lower() not in {".pdb", ".cif", ".pdbqt"}:
            return "small_molecule"
        return self.detect_receptor_type(file_path if file_path.suffix.lower() == ".pdb" else self.convert_to_pdb(file_path))

    def convert_to_pdb(self, path: Path) -> Path:
        if path.suffix.lower() == ".pdb":
            return path
        obabel = self.tool("obabel_bin")
        tmpdir = Path(tempfile.mkdtemp(prefix="drugclaw_dock_"))
        out = tmpdir / f"{slugify(path.stem)}.pdb"
        run_command([obabel, str(path), "-O", str(out), "-opdb"], timeout=1200)
        return out

    def repair_biomolecule_pdb(self, pdb_path: Path) -> Optional[Path]:
        lines = pdb_path.read_text(encoding="utf-8", errors="ignore").splitlines()
        repaired = []
        atom_count = 0
        for line in lines:
            if line.startswith(("ATOM", "HETATM", "HEADER", "TITLE", "COMPND")):
                repaired.append(line)
                if line.startswith(("ATOM", "HETATM")):
                    atom_count += 1
        if atom_count == 0:
            return None
        if not repaired or repaired[-1] != "END":
            repaired.append("END")
        handle, temp_path = tempfile.mkstemp(suffix=".pdb", prefix="drugclaw_repair_")
        os.close(handle)
        Path(temp_path).write_text("\n".join(repaired) + "\n", encoding="utf-8")
        return Path(temp_path)

    def process_ligand_biomolecule(self, input_file: Path, output_pdbqt: Path, notes: list[str]) -> str:
        obabel = self.tool("obabel_bin")
        try:
            run_command([obabel, str(input_file), "-O", str(output_pdbqt)], timeout=1200)
            if self.validate_pdbqt(output_pdbqt):
                return "biomolecule_simple"
        except Exception:
            pass
        repaired = self.repair_biomolecule_pdb(self.convert_to_pdb(input_file))
        if repaired:
            try:
                run_command([obabel, str(repaired), "-O", str(output_pdbqt)], timeout=1200)
                if self.validate_pdbqt(output_pdbqt):
                    notes.append(f"repaired biomolecule PDB: {repaired}")
                    return "biomolecule_repaired"
            finally:
                if repaired.exists():
                    repaired.unlink()
        pymol = which_or_override("pymol", self.settings.get("pymol_bin"))
        if pymol:
            temp_pdb = output_pdbqt.with_suffix(".tmp.pdb")
            script_path = output_pdbqt.with_suffix(".prep.pml")
            script = f"load {input_file}\nsave {temp_pdb}\nquit\n"
            write_text(script_path, script)
            try:
                run_command([pymol, "-cq", str(script_path)], timeout=1200)
                if temp_pdb.exists():
                    run_command([obabel, str(temp_pdb), "-O", str(output_pdbqt)], timeout=1200)
                    if self.validate_pdbqt(output_pdbqt):
                        return "biomolecule_pymol"
            finally:
                for path in [temp_pdb, script_path]:
                    if path.exists():
                        path.unlink()
        for label, extra_args in [
            ("add_h", ["-h"]),
            ("drop_h", ["-d"]),
            ("polar_h", ["-p"]),
            ("no_charge", ["--partialcharge", "none"]),
        ]:
            try:
                run_command([obabel, str(input_file), "-O", str(output_pdbqt), *extra_args], timeout=1200)
                if self.validate_pdbqt(output_pdbqt):
                    return f"biomolecule_{label}"
            except Exception:
                continue
        raise PipelineError(f"Could not prepare biomolecule ligand: {input_file}")

    def process_ligand_small_molecule(self, input_file: Path, output_pdbqt: Path, notes: list[str]) -> str:
        obabel = self.tool("obabel_bin")
        current_input = input_file
        temp_paths: list[Path] = []
        if self.is_2d_structure(input_file):
            temp_3d = output_pdbqt.with_suffix(".3d.sdf")
            run_command([obabel, str(input_file), "-osdf", "-O", str(temp_3d), "--gen3d", "--fast"], timeout=300)
            if temp_3d.exists() and temp_3d.stat().st_size > 50:
                current_input = temp_3d
                temp_paths.append(temp_3d)
                notes.append("generated 3D coordinates from 2D input")
        forcefields = ["MMFF94s", "UFF", "GAFF", "MMFF94", "Ghemical"]
        for ff in forcefields:
            try:
                run_command(
                    [
                        obabel,
                        str(current_input),
                        "-O",
                        str(output_pdbqt),
                        "--minimize",
                        "--forcefield",
                        ff,
                        "--steps",
                        "200",
                        "--convergence",
                        "1e-4",
                        "--partialcharge",
                        "gasteiger",
                    ],
                    timeout=1200,
                )
                if self.validate_pdbqt(output_pdbqt):
                    for temp in temp_paths:
                        if temp.exists():
                            temp.unlink()
                    return f"small_molecule_{ff}"
            except Exception:
                continue
        for label, args in [
            ("simple_charged", ["--partialcharge", "gasteiger"]),
            ("simple", []),
        ]:
            try:
                run_command([obabel, str(current_input), "-O", str(output_pdbqt), *args], timeout=1200)
                if self.validate_pdbqt(output_pdbqt):
                    for temp in temp_paths:
                        if temp.exists():
                            temp.unlink()
                    return f"small_molecule_{label}"
            except Exception:
                continue
        raise PipelineError(f"Could not prepare small-molecule ligand: {input_file}")

    def prepare_ligands(self) -> dict[str, LigandRecord]:
        if not self.session["ligands"]:
            self.fetch_ligands()
        prepared: dict[str, LigandRecord] = {}
        for slug, record_data in self.session["ligands"].items():
            record = LigandRecord(**record_data)
            raw_path = Path(record.raw_path)
            output_pdbqt = self.workspace.prepared_ligands / f"{slug}.pdbqt"
            if raw_path.suffix.lower() == ".pdbqt":
                copy_file(raw_path, output_pdbqt)
                prep_method = "already_pdbqt"
                ligand_type = self.detect_biomolecule(raw_path)
            else:
                ligand_type = self.detect_biomolecule(raw_path)
                if ligand_type in {"protein", "peptide", "nucleic"}:
                    prep_method = self.process_ligand_biomolecule(raw_path, output_pdbqt, record.notes)
                else:
                    prep_method = self.process_ligand_small_molecule(raw_path, output_pdbqt, record.notes)
            if not self.validate_pdbqt(output_pdbqt):
                raise PipelineError(f"Invalid prepared ligand PDBQT: {output_pdbqt}")
            record.prepared_pdbqt_path = str(output_pdbqt)
            record.ligand_type = ligand_type
            record.prep_method = prep_method
            self.session["ligands"][slug] = asdict(record)
            prepared[slug] = record
            log(f"Prepared ligand {record.name}: {output_pdbqt} ({prep_method})")
        self.append_history("prepare_ligands", "success", {"count": len(prepared)})
        self.save_session()
        return prepared

    def parse_atom_coords(self, file_path: Path, tags: tuple[str, ...] = ("ATOM", "HETATM")) -> list[tuple[float, float, float]]:
        coords: list[tuple[float, float, float]] = []
        with file_path.open("r", encoding="utf-8", errors="ignore") as handle:
            for line in handle:
                if line.startswith(tags):
                    try:
                        coords.append(
                            (
                                float(line[30:38].strip()),
                                float(line[38:46].strip()),
                                float(line[46:54].strip()),
                            )
                        )
                    except Exception:
                        continue
        return coords

    def extract_cocrystal_coords(self, file_path: Path) -> list[tuple[float, float, float]]:
        coords: list[tuple[float, float, float]] = []
        with file_path.open("r", encoding="utf-8", errors="ignore") as handle:
            for line in handle:
                if line.startswith("HETATM") and line[17:20].strip().upper() != "HOH":
                    try:
                        coords.append(
                            (
                                float(line[30:38].strip()),
                                float(line[38:46].strip()),
                                float(line[46:54].strip()),
                            )
                        )
                    except Exception:
                        continue
        return coords

    def extract_active_residues(self, file_path: Path) -> list[dict[str, Any]]:
        residues: list[dict[str, Any]] = []
        with file_path.open("r", encoding="utf-8", errors="ignore") as handle:
            for line in handle:
                if line.startswith("ATOM"):
                    res_name = line[17:20].strip().upper()
                    atom_name = line[12:16].strip().upper()
                    if res_name in ACTIVE_RESIDUES and atom_name == "CA":
                        try:
                            residues.append(
                                {
                                    "id": f"{res_name}{line[22:26].strip()}",
                                    "x": float(line[30:38].strip()),
                                    "y": float(line[38:46].strip()),
                                    "z": float(line[46:54].strip()),
                                }
                            )
                        except Exception:
                            continue
        return residues

    def calculate_box_from_coords(self, coords: Iterable[tuple[float, float, float]], padding: float) -> tuple[tuple[float, float, float], tuple[float, float, float]]:
        coord_list = list(coords)
        if not coord_list:
            raise PipelineError("No coordinates available for box calculation")
        xs = [c[0] for c in coord_list]
        ys = [c[1] for c in coord_list]
        zs = [c[2] for c in coord_list]
        center = ((min(xs) + max(xs)) / 2.0, (min(ys) + max(ys)) / 2.0, (min(zs) + max(zs)) / 2.0)
        size = (
            max(max(xs) - min(xs) + padding, 10.0),
            max(max(ys) - min(ys) + padding, 10.0),
            max(max(zs) - min(zs) + padding, 10.0),
        )
        return center, size

    def calculate_coverage(self, center: tuple[float, float, float], size: tuple[float, float, float], residues: list[dict[str, Any]]) -> float:
        if not residues:
            return 0.0
        half = [s / 2.0 for s in size]
        inside = 0
        for residue in residues:
            if (
                center[0] - half[0] <= residue["x"] <= center[0] + half[0]
                and center[1] - half[1] <= residue["y"] <= center[1] + half[1]
                and center[2] - half[2] <= residue["z"] <= center[2] + half[2]
            ):
                inside += 1
        return (inside / len(residues)) * 100.0

    def adjust_box(self, center: tuple[float, float, float], size: tuple[float, float, float], residues: list[dict[str, Any]]) -> tuple[tuple[float, float, float], tuple[float, float, float], float]:
        target = float(self.settings.get("box_coverage", 0.90)) * 100.0
        min_size, max_size = [float(x) for x in self.settings.get("box_size_range", [18.0, 42.0])]
        max_volume = float(self.settings.get("max_volume", 27000.0))
        current = [max(size[0], 10.0), max(size[1], 10.0), max(size[2], 10.0)]
        coverage = self.calculate_coverage(center, tuple(current), residues)
        for _ in range(20):
            volume = current[0] * current[1] * current[2]
            if self.settings.get("limit_volume") and volume > max_volume:
                scale = (max_volume / volume) ** (1 / 3)
                current = [max(10.0, dim * scale) for dim in current]
                coverage = self.calculate_coverage(center, tuple(current), residues)
                continue
            if abs(coverage - target) <= 5.0 or not residues:
                break
            if coverage < target:
                candidate = [min(max_size, dim * 1.1) for dim in current]
            else:
                candidate = [max(min_size, dim * 0.9) for dim in current]
            next_coverage = self.calculate_coverage(center, tuple(candidate), residues)
            if coverage < target and next_coverage < coverage:
                break
            if coverage > target and next_coverage < target:
                break
            current = candidate
            coverage = next_coverage
        return center, tuple(current), coverage

    def manual_box_from_manifest(self, item: dict[str, Any]) -> Optional[BoxParams]:
        box = item.get("box")
        if not box:
            return None
        center = list(box.get("center") or [box.get("center_x"), box.get("center_y"), box.get("center_z")])
        size = list(box.get("size") or [box.get("size_x"), box.get("size_y"), box.get("size_z")])
        if any(value is None for value in center + size):
            return None
        return BoxParams(
            center_x=float(center[0]),
            center_y=float(center[1]),
            center_z=float(center[2]),
            size_x=float(size[0]),
            size_y=float(size[1]),
            size_z=float(size[2]),
            mode=box.get("mode", "manual"),
            coverage=0.0,
        )

    def build_box_for_receptor(self, item: dict[str, Any], record: ReceptorRecord) -> BoxParams:
        manual = self.manual_box_from_manifest(item)
        if manual:
            return manual
        padding = float(self.settings.get("box_padding", 8.0))
        raw_path = Path(record.raw_path)
        prepared_pdb_path = Path(record.prepared_pdb_path)
        cocrystal = self.extract_cocrystal_coords(raw_path if raw_path.exists() else prepared_pdb_path)
        if cocrystal:
            center, size = self.calculate_box_from_coords(cocrystal, padding)
            residues = self.extract_active_residues(raw_path if raw_path.exists() else prepared_pdb_path)
            center, size, coverage = self.adjust_box(center, size, residues)
            return BoxParams(*center, *size, mode="cocrystal", coverage=coverage)
        residues = self.extract_active_residues(raw_path if raw_path.exists() else prepared_pdb_path)
        if residues:
            coords = [(r["x"], r["y"], r["z"]) for r in residues]
            center, size = self.calculate_box_from_coords(coords, padding)
            center, size, coverage = self.adjust_box(center, size, residues)
            return BoxParams(*center, *size, mode="active_residues", coverage=coverage)
        coords = self.parse_atom_coords(Path(record.prepared_pdbqt_path))
        if not coords:
            coords = self.parse_atom_coords(prepared_pdb_path)
        center, size = self.calculate_box_from_coords(coords, padding)
        if self.settings.get("limit_volume"):
            volume = size[0] * size[1] * size[2]
            max_volume = float(self.settings.get("max_volume", 27000.0))
            if volume > max_volume:
                scale = (max_volume / volume) ** (1 / 3)
                size = tuple(max(10.0, dim * scale) for dim in size)
        return BoxParams(*center, *size, mode="bounding_box", coverage=0.0)

    def generate_boxes(self) -> dict[str, BoxParams]:
        if not self.session["receptors"] or any(not data.get("prepared_pdbqt_path") for data in self.session["receptors"].values()):
            self.prepare_receptors()
        boxes: dict[str, BoxParams] = {}
        manifest_items = {slugify((item.get("name") or item.get("identifier") or item.get("path"))): item for item in self.manifest_receptors()}
        for slug, record_data in self.session["receptors"].items():
            record = ReceptorRecord(**record_data)
            item = manifest_items.get(slug, {})
            box = self.build_box_for_receptor(item, record)
            template_path = self.workspace.receptor_template_path(slug)
            template = box.as_template(Path(record.prepared_pdbqt_path)).replace("{exhaustiveness}", str(self.settings["exhaustiveness"]))
            template = template.replace("{num_modes}", str(self.settings["num_modes"]))
            template = template.replace("{energy_range}", str(self.settings["energy_range"]))
            write_text(template_path, template)
            record.box = asdict(box)
            self.session["receptors"][slug] = asdict(record)
            boxes[slug] = box
            log(
                f"Box for {record.name}: center=({box.center_x:.2f}, {box.center_y:.2f}, {box.center_z:.2f}) "
                f"size=({box.size_x:.2f}, {box.size_y:.2f}, {box.size_z:.2f}) mode={box.mode} coverage={box.coverage:.1f}%"
            )
        self.append_history("generate_boxes", "success", {"count": len(boxes)})
        self.save_session()
        return boxes

