    def ligand_descriptor_map(self) -> dict[str, dict[str, float]]:
        descriptors: dict[str, dict[str, float]] = {}
        try:
            from rdkit import Chem
            from rdkit.Chem import Descriptors, rdMolDescriptors
        except Exception:
            return descriptors
        for slug, data in self.session["ligands"].items():
            smiles = data.get("smiles")
            if not smiles:
                continue
            mol = Chem.MolFromSmiles(smiles)
            if mol is None:
                continue
            descriptors[slug] = {
                "mol_wt": float(Descriptors.MolWt(mol)),
                "logp": float(Descriptors.MolLogP(mol)),
                "h_donors": float(rdMolDescriptors.CalcNumHBD(mol)),
                "h_acceptors": float(rdMolDescriptors.CalcNumHBA(mol)),
                "tpsa": float(rdMolDescriptors.CalcTPSA(mol)),
                "rot_bonds": float(rdMolDescriptors.CalcNumRotatableBonds(mol)),
            }
        return descriptors

    def heuristic_ml_scores(self, summary_rows: list[dict[str, Any]]) -> tuple[list[dict[str, Any]], dict[str, Any]]:
        try:
            import pandas as pd
            from sklearn.ensemble import RandomForestRegressor
            from sklearn.impute import SimpleImputer
            from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score
            from sklearn.model_selection import cross_val_score, train_test_split
            from sklearn.preprocessing import StandardScaler
        except Exception:
            return [], {}
        descriptor_map = self.ligand_descriptor_map()
        if not descriptor_map:
            return [], {}
        scores_by_ligand: dict[str, list[float]] = {}
        for row in summary_rows:
            score = row.get("score_kcal_mol")
            if score is None:
                continue
            scores_by_ligand.setdefault(row["ligand"], []).append(float(score))
        rows = []
        for ligand, scores in scores_by_ligand.items():
            if ligand not in descriptor_map:
                continue
            row = {**descriptor_map[ligand], "target": sum(scores) / len(scores), "ligand": ligand}
            rows.append(row)
        if len(rows) < 10:
            return [], {}
        frame = pd.DataFrame(rows)
        X = frame.drop(columns=["target", "ligand"])
        y = frame["target"]
        imputer = SimpleImputer(strategy="median")
        X_imputed = imputer.fit_transform(X)
        scaler = StandardScaler()
        X_scaled = scaler.fit_transform(X_imputed)
        X_train, X_test, y_train, y_test = train_test_split(X_scaled, y, test_size=0.2, random_state=42)
        model = RandomForestRegressor(n_estimators=200, max_depth=20, min_samples_split=4, random_state=42, n_jobs=-1)
        model.fit(X_train, y_train)
        y_pred = model.predict(X_test)
        cv = cross_val_score(model, X_scaled, y, cv=5, scoring="r2")
        metrics = {
            "r2": float(r2_score(y_test, y_pred)),
            "mse": float(mean_squared_error(y_test, y_pred)),
            "mae": float(mean_absolute_error(y_test, y_pred)),
            "cv_r2_mean": float(cv.mean()),
            "cv_r2_std": float(cv.std()),
            "feature_importance": dict(zip(X.columns.tolist(), [float(x) for x in model.feature_importances_])),
        }
        frame["predicted_mean_score"] = model.predict(X_scaled)
        return frame.to_dict(orient="records"), metrics

