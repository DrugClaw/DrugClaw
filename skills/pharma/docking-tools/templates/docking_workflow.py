    def fetch_drugbank_ligand(self, *, slug: str, name: str, identifier: str) -> tuple[Path, str, Path, list[str]]:
        hits_csv = self.workspace.raw_ligands / f"{slug}.drugbank_hits.csv"
        summary_json = self.workspace.raw_ligands / f"{slug}.drugbank_summary.json"
        top_hit_json = self.workspace.raw_ligands / f"{slug}.drugbank.json"
        sdf_path = self.workspace.raw_ligands / f"{slug}.sdf"
        query_value = identifier or name
        args = [
            "--output",
            str(hits_csv),
            "--summary",
            str(summary_json),
            "--top-hit-json",
            str(top_hit_json),
            "--sdf-output",
            str(sdf_path),
            "--copy-structure",
        ]
        env: dict[str, str] = {}
        configured_catalog = str(self.settings.get("drugbank_catalog") or "").strip()
        if configured_catalog:
            catalog = self.require_drugbank_catalog()
            args.extend(["--mode", "local", "--catalog", str(catalog)])
        elif self.drugbank_api_key() or self.drugbank_api_token():
            args.extend(["--mode", "online"])
            api_base_url = self.drugbank_api_base_url()
            if api_base_url:
                args.extend(["--api-base-url", api_base_url])
            if self.drugbank_api_key():
                env["DRUGBANK_API_KEY"] = self.drugbank_api_key()
            if self.drugbank_api_token():
                env["DRUGBANK_API_TOKEN"] = self.drugbank_api_token()
        else:
            raise PipelineError(
                "DrugBank fetch requires either settings.drugbank_catalog or DRUGBANK_API_KEY/DRUGBANK_API_TOKEN "
                "(or matching values in manifest settings)"
            )
        if query_value.upper().startswith("DB"):
            args.extend(["--drugbank-id", query_value.upper()])
        else:
            args.extend(["--query", query_value])
        self.run_python_template(self.chem_template_path("drugbank_lookup.py"), args, timeout=1800, env=env or None)
        if not top_hit_json.exists():
            raise PipelineError(f"DrugBank lookup returned no top hit for {query_value}")
        payload = read_json(top_hit_json)
        smiles = str(payload.get("smiles") or "").strip()
        notes = []
        matched_id = str(payload.get("drugbank_id") or "").strip()
        if matched_id:
            notes.append(f"DrugBank match: {matched_id}")
        if str(payload.get("name") or "").strip():
            notes.append(f"DrugBank name: {payload['name']}")
        if not sdf_path.exists():
            pubchem_cid = str(payload.get("pubchem_cid") or "").strip()
            chembl_id = str(payload.get("chembl_id") or "").strip()
            if pubchem_cid:
                self.download_text(
                    f"https://pubchem.ncbi.nlm.nih.gov/rest/pug/compound/cid/{pubchem_cid}/SDF",
                    sdf_path,
                    "pubchem",
                )
                notes.append(f"structure fetched via PubChem CID {pubchem_cid}")
            elif chembl_id:
                self.download_text(
                    f"https://www.ebi.ac.uk/chembl/api/data/molecule/{chembl_id.upper()}.sdf",
                    sdf_path,
                    "chembl",
                )
                notes.append(f"structure fetched via ChEMBL {chembl_id.upper()}")
            elif smiles:
                self.smiles_to_sdf(smiles, sdf_path)
                notes.append("structure generated from DrugBank SMILES")
        if not sdf_path.exists():
            raise PipelineError(f"DrugBank hit for {query_value} did not provide an exportable structure")
        return sdf_path, smiles, top_hit_json, notes

    def smiles_to_sdf(self, smiles: str, dest: Path) -> Path:
        ensure_dir(dest.parent)
        try:
            from rdkit import Chem
            from rdkit.Chem import AllChem

            mol = Chem.MolFromSmiles(smiles)
            if mol is None:
                raise PipelineError(f"Invalid SMILES: {smiles}")
            mol = Chem.AddHs(mol)
            AllChem.EmbedMolecule(mol, randomSeed=0xF00D)
            AllChem.MMFFOptimizeMolecule(mol)
            writer = Chem.SDWriter(str(dest))
            writer.write(mol)
            writer.close()
            return dest
        except Exception as exc:
            log(f"RDKit SMILES generation failed, falling back to obabel: {exc}", "WARNING")
            obabel = self.tool("obabel_bin")
            run_command([obabel, f"-:{smiles}", "-osdf", "-O", str(dest), "--gen3d"], timeout=300)
            return dest

    def sequence_to_pdb(self, sequence: str, kind: str, dest: Path) -> Path:
        from Bio.PDB import PDBIO, StructureBuilder

        ensure_dir(dest.parent)
        builder = StructureBuilder.StructureBuilder()
        builder.init_structure(kind)
        builder.init_model(0)
        builder.init_chain("A")
        clean = re.sub(r"\s+", "", sequence).upper()
        if kind in {"peptide", "protein", "protein_sequence"}:
            for index, residue in enumerate(clean, 1):
                if residue not in AA_MAP:
                    raise PipelineError(f"Unsupported amino acid residue: {residue}")
                builder.init_residue(AA_MAP[residue], " ", index, " ")
                builder.init_atom("CA", [0.0, index * 3.8, 0.0], 1.0, 1.0, " ", "CA")
        elif kind in {"nucleic", "nucleic_sequence"}:
            for index, base in enumerate(clean, 1):
                if base not in {"A", "C", "G", "T", "U"}:
                    raise PipelineError(f"Unsupported nucleic base: {base}")
                builder.init_residue(base, " ", index, " ")
                builder.init_atom("P", [0.0, 0.0, index * 3.4], 1.0, 1.0, " ", "P")
        else:
            raise PipelineError(f"Unsupported sequence kind: {kind}")
        io = PDBIO()
        io.set_structure(builder.get_structure())
        io.save(str(dest))
        return dest

    def fetch_ligands(self) -> dict[str, LigandRecord]:
        records: dict[str, LigandRecord] = {}
        for item in self.manifest_ligands():
            name = item.get("name") or item.get("identifier") or item.get("smiles")
            source, identifier = self.resolve_ligand_item(item)
            slug = slugify(name)
            notes: list[str] = []
            smiles = item.get("smiles", "")
            properties_path = ""
            if source == "local":
                src = Path(identifier).expanduser().resolve()
                if not src.exists():
                    raise PipelineError(f"Ligand file not found: {src}")
                raw_path = copy_file(src, self.workspace.raw_ligands / f"{slug}{src.suffix.lower()}")
            elif source == "smiles":
                raw_path = self.smiles_to_sdf(identifier, self.workspace.raw_ligands / f"{slug}.sdf")
                smiles = identifier
                notes.append("generated from SMILES")
            elif source == "pubchem":
                raw_path = self.download_text(
                    f"https://pubchem.ncbi.nlm.nih.gov/rest/pug/compound/cid/{identifier}/SDF",
                    self.workspace.raw_ligands / f"{slug}.sdf",
                    "pubchem",
                )
            elif source == "chembl":
                raw_path = self.download_text(
                    f"https://www.ebi.ac.uk/chembl/api/data/molecule/{identifier.upper()}.sdf",
                    self.workspace.raw_ligands / f"{slug}.sdf",
                    "chembl",
                )
            elif source == "zinc":
                raw_path = self.download_text(
                    f"https://zinc15.docking.org/substances/{identifier.upper()}.sdf",
                    self.workspace.raw_ligands / f"{slug}.sdf",
                    "zinc",
                )
            elif source == "tcmsp":
                raw_path = self.download_text(
                    f"http://tcmspw.com/tcmspdownload.php?mol_id={identifier.upper()}",
                    self.workspace.raw_ligands / f"{slug}.mol2",
                    "tcmsp",
                )
            elif source == "drugbank":
                raw_path, smiles, properties_json, extra_notes = self.fetch_drugbank_ligand(slug=slug, name=name, identifier=identifier)
                properties_path = str(properties_json)
                notes.extend(extra_notes)
            else:
                raise PipelineError(f"Unsupported ligand source: {source}")
            record = LigandRecord(
                name=name,
                slug=slug,
                source=source,
                identifier=identifier,
                raw_path=str(raw_path),
                properties_path=properties_path,
                smiles=smiles,
                notes=notes,
            )
            records[slug] = record
            self.session["ligands"][slug] = asdict(record)
            log(f"Ligand ready: {name} -> {raw_path}")
        self.append_history("fetch_ligands", "success", {"count": len(records)})
        self.save_session()
        return records

