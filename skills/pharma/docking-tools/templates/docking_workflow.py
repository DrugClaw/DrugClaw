def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="DrugClaw docking workflow toolkit")
    sub = parser.add_subparsers(dest="command", required=True)

    init_manifest = sub.add_parser("init-manifest", help="write an example workflow manifest")
    init_manifest.add_argument("-o", "--output", default="docking_manifest.example.json")

    doctor = sub.add_parser("doctor", help="check runtime dependencies for the current workflow")
    doctor.add_argument("--manifest", required=False)
    doctor.add_argument("--strict", action="store_true", help="also fail on optional plotting and chemistry extras")

    for name in ["fetch", "prepare", "box", "dock", "analyze", "render", "run"]:
        command = sub.add_parser(name)
        command.add_argument("--manifest", required=True)
    prepare = [p for p in sub.choices.values() if p.prog.endswith(" prepare")][0]
    prepare.add_argument("--receptors-only", action="store_true")
    prepare.add_argument("--ligands-only", action="store_true")
    render = [p for p in sub.choices.values() if p.prog.endswith(" render")][0]
    render.add_argument("--top-n", type=int, default=None)
    run_parser = [p for p in sub.choices.values() if p.prog.endswith(" run")][0]
    run_parser.add_argument("--skip-fetch", action="store_true")
    run_parser.add_argument("--skip-prepare", action="store_true")
    run_parser.add_argument("--skip-box", action="store_true")
    run_parser.add_argument("--skip-dock", action="store_true")
    run_parser.add_argument("--skip-analyze", action="store_true")
    run_parser.add_argument("--skip-render", action="store_true")
    return parser


def main(argv: Optional[list[str]] = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)
    if args.command == "init-manifest":
        payload = example_manifest()
        write_json(Path(args.output), payload)
        log(f"Wrote example manifest to {args.output}")
        return 0
    if args.command == "doctor":
        if args.manifest:
            workflow = DockingWorkflow(Path(args.manifest), create_workspace=False)
        else:
            with tempfile.NamedTemporaryFile("w", suffix=".json", delete=False, encoding="utf-8") as handle:
                json.dump(example_manifest(), handle, indent=2, ensure_ascii=False)
                temp_manifest = Path(handle.name)
            try:
                workflow = DockingWorkflow(temp_manifest, create_workspace=False)
            finally:
                temp_manifest.unlink(missing_ok=True)
        return 0 if workflow.ensure_doctor(strict=args.strict) else 1
    workflow = DockingWorkflow(Path(args.manifest))
    try:
        if args.command == "fetch":
            workflow.fetch_inputs()
        elif args.command == "prepare":
            if args.receptors_only and args.ligands_only:
                raise PipelineError("Cannot use both --receptors-only and --ligands-only")
            if not args.ligands_only:
                workflow.prepare_receptors()
            if not args.receptors_only:
                workflow.prepare_ligands()
        elif args.command == "box":
            workflow.generate_boxes()
        elif args.command == "dock":
            workflow.run_docking()
        elif args.command == "analyze":
            outputs = workflow.analyze_results()
            for key, value in outputs.items():
                log(f"{key}: {value}")
        elif args.command == "render":
            workflow.render_top_hits(args.top_n)
        elif args.command == "run":
            workflow.run_all(
                skip_fetch=args.skip_fetch,
                skip_prepare=args.skip_prepare,
                skip_box=args.skip_box,
                skip_dock=args.skip_dock,
                skip_analyze=args.skip_analyze,
                skip_render=args.skip_render,
            )
        else:
            parser.error(f"Unknown command: {args.command}")
    except PipelineError as exc:
        log(str(exc), "ERROR")
        return 2
    except Exception as exc:
        log(str(exc), "ERROR")
        return 3
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
