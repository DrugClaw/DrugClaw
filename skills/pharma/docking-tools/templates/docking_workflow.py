class Workspace:
    def __init__(self, root: Path, *, create: bool = True):
        make = ensure_dir if create else (lambda path: path)
        self.root = root.resolve()
        self.inputs = make(self.root / "inputs")
        self.raw_receptors = make(self.inputs / "receptors")
        self.raw_ligands = make(self.inputs / "ligands")
        self.generated = make(self.inputs / "generated")
        self.prepared = make(self.root / "prepared")
        self.prepared_receptors = make(self.prepared / "receptors")
        self.prepared_ligands = make(self.prepared / "ligands")
        self.configs = make(self.root / "configs")
        self.receptor_configs = make(self.configs / "receptors")
        self.results = make(self.root / "results")
        self.docking = make(self.results / "docking")
        self.complexes = make(self.results / "complexes")
        self.renders = make(self.results / "renders")
        self.analysis = make(self.results / "analysis")
        self.metadata = make(self.root / "metadata")
        self.session_path = self.metadata / "session.json"
        self.history_path = self.metadata / "history.jsonl"

    def receptor_dir(self, slug: str) -> Path:
        return ensure_dir(self.prepared_receptors / slug)

    def receptor_template_path(self, slug: str) -> Path:
        return self.receptor_configs / f"{slug}.template.conf"

    def docking_pair_config_path(self, receptor_slug: str, ligand_slug: str) -> Path:
        return self.docking / f"{receptor_slug}_vs_{ligand_slug}.conf"

    def docking_output_path(self, receptor_slug: str, ligand_slug: str) -> Path:
        return self.docking / f"{receptor_slug}_vs_{ligand_slug}.pdbqt"

    def docking_log_path(self, receptor_slug: str, ligand_slug: str) -> Path:
        return self.docking / f"{receptor_slug}_vs_{ligand_slug}.log"

    def complex_path(self, receptor_slug: str, ligand_slug: str) -> Path:
        return self.complexes / f"{receptor_slug}_vs_{ligand_slug}.pdb"

    def render_script_path(self, receptor_slug: str, ligand_slug: str) -> Path:
        return self.renders / f"{receptor_slug}_vs_{ligand_slug}.pml"

    def render_png_path(self, receptor_slug: str, ligand_slug: str) -> Path:
        return self.renders / f"{receptor_slug}_vs_{ligand_slug}.png"


class DockingWorkflow:
    def __init__(self, manifest_path: Path, *, create_workspace: bool = True):
        self.manifest_path = manifest_path.resolve()
        self.manifest = read_json(self.manifest_path)
        self.settings = {**DEFAULT_SETTINGS, **self.manifest.get("settings", {})}
        workspace_root = Path(self.manifest.get("workspace") or "./docking")
        if not workspace_root.is_absolute():
            workspace_root = self.manifest_path.parent / workspace_root
        self.workspace = Workspace(workspace_root, create=create_workspace)
        self.session = self._load_session()
        self.http = requests.Session() if requests else None
        if self.http:
            self.http.headers.update({"User-Agent": "DrugClaw docking workflow/1.0"})

    def _load_session(self) -> dict[str, Any]:
        if self.workspace.session_path.exists():
            return read_json(self.workspace.session_path)
        return {
            "manifest_path": str(self.manifest_path),
            "workspace": str(self.workspace.root),
            "updated_at": now_ts(),
            "settings": self.settings,
            "receptors": {},
            "ligands": {},
            "docking": [],
        }

    def save_session(self) -> None:
        self.session["updated_at"] = now_ts()
        self.session["settings"] = self.settings
        write_json(self.workspace.session_path, self.session)

    def append_history(self, step: str, status: str, detail: dict[str, Any]) -> None:
        record = {"time": now_ts(), "step": step, "status": status, "detail": detail}
        ensure_dir(self.workspace.history_path.parent)
        with self.workspace.history_path.open("a", encoding="utf-8") as handle:
            handle.write(json.dumps(record, ensure_ascii=False) + "\n")

    def require_requests(self) -> None:
        if self.http is None:
            raise PipelineError("requests is not available; remote fetch/query features are disabled")

    def tool(self, key: str) -> str:
        override = self.settings.get(key)
        default_name = DEFAULT_SETTINGS[key]
        resolved = which_or_override(default_name, override)
        if not resolved:
            raise PipelineError(f"Required tool not found: {default_name}")
        return resolved

    def manifest_receptors(self) -> list[dict[str, Any]]:
        return list(self.manifest.get("receptors", []))

    def manifest_ligands(self) -> list[dict[str, Any]]:
        return list(self.manifest.get("ligands", []))

    def requires_http_features(self) -> bool:
        for item in self.manifest_ligands():
            source = str(item.get("source") or "auto").lower()
            if source == "auto":
                identifier = str(item.get("identifier") or "").strip()
                if item.get("path") or item.get("smiles"):
                    continue
                if not identifier:
                    return True
                if identifier.upper().startswith("DB") and not (self.drugbank_api_key() or self.drugbank_api_token()):
                    continue
                if identifier:
                    return True
            elif source in REMOTE_LIGAND_SOURCES:
                if source != "drugbank":
                    return True
                if self.drugbank_api_key() or self.drugbank_api_token():
                    return True
        for item in self.manifest_receptors():
            source = str(item.get("source") or "auto").lower()
            if source == "auto":
                identifier = str(item.get("identifier") or "").strip()
                if not item.get("path") and identifier:
                    return True
            elif source in REMOTE_RECEPTOR_SOURCES:
                return True
        return False

    def manifest_uses_drugbank(self) -> bool:
        for item in self.manifest_ligands():
            source = str(item.get("source") or "auto").lower()
            identifier = str(item.get("identifier") or "").strip()
            if source == "drugbank":
                return True
            if source == "auto" and identifier.upper().startswith("DB") and self.has_drugbank_lookup():
                return True
        return False

    def required_chem_template_names(self) -> set[str]:
        required: set[str] = set()
        if self.manifest_uses_drugbank():
            required.add("drugbank_lookup.py")
        chem_postprocess = dict(self.manifest.get("chem_postprocess") or {})
        if bool(chem_postprocess.get("enabled")):
            if chem_postprocess.get("run_admet", True):
                required.add("admet_screen.py")
            if chem_postprocess.get("affinity_model"):
                required.add("binding_affinity_predict.py")
            if chem_postprocess.get("structure_affinity_model"):
                required.add("protein_ligand_affinity.py")
            if chem_postprocess.get("bioactivity_model"):
                required.add("bioactivity_predict.py")
            if chem_postprocess.get("run_virtual_screen", True):
                required.add("virtual_screen.py")
        return required

    def required_python_modules(self) -> set[str]:
        required: set[str] = set()
        if self.requires_http_features():
            required.add("requests")
        if any(str(item.get("source") or "auto").lower() in SEQUENCE_SOURCES for item in self.manifest_receptors()):
            required.add("Bio")
        if self.manifest_uses_drugbank():
            required.update({"pandas", "rdkit"})
            if self.drugbank_api_key() or self.drugbank_api_token():
                required.add("requests")
        chem_postprocess = dict(self.manifest.get("chem_postprocess") or {})
        if bool(chem_postprocess.get("enabled")):
            required.update({"pandas", "rdkit"})
            if chem_postprocess.get("run_admet", True):
                required.update({"pandas", "rdkit"})
            if chem_postprocess.get("affinity_model") or chem_postprocess.get("bioactivity_model"):
                required.update({"joblib", "numpy", "pandas", "rdkit", "sklearn"})
            if chem_postprocess.get("structure_affinity_model"):
                required.update({"joblib", "numpy", "pandas"})
            if chem_postprocess.get("run_virtual_screen", True):
                required.add("pandas")
        return required

    def ensure_doctor(self, strict: bool = False) -> bool:
        missing: list[str] = []
        required_modules = self.required_python_modules()
        required_chem_templates = self.required_chem_template_names()
        binaries = {
            "vina": self.settings.get("vina_bin"),
            "obabel": self.settings.get("obabel_bin"),
            "pymol": self.settings.get("pymol_bin"),
            "pdbfixer": self.settings.get("pdbfixer_bin"),
        }
        for logical, configured in binaries.items():
            path = which_or_override(logical, configured)
            if path:
                log(f"binary {logical}: {path}")
            else:
                log(f"binary {logical}: missing", "WARNING")
                if logical in {"vina", "obabel", "pdbfixer"}:
                    missing.append(logical)
        for name in MODULE_CHECKS:
            if importlib.util.find_spec(name) is not None:
                log(f"python module {name}: ok")
            else:
                log(f"python module {name}: missing", "WARNING")
                if name in required_modules:
                    missing.append(name)
                elif strict and name not in STRICT_ONLY_MODULES:
                    missing.append(name)
        if required_chem_templates:
            try:
                chem_dir = self.chem_templates_dir()
                log(f"chem-tools templates: {chem_dir}")
                for template_name in sorted(required_chem_templates):
                    template_path = chem_dir / template_name
                    if template_path.exists():
                        log(f"chem template {template_name}: ok")
                    else:
                        log(f"chem template {template_name}: missing", "ERROR")
                        missing.append(f"chem template {template_name}")
            except PipelineError as exc:
                log(str(exc), "ERROR")
                missing.append("chem-tools templates")
        if missing:
            log("Missing required runtime pieces: " + ", ".join(sorted(set(missing))), "ERROR")
            return False
        return True

    def resolve_ligand_item(self, item: dict[str, Any]) -> tuple[str, str]:
        name = item.get("name") or item.get("identifier") or item.get("smiles")
        if not name:
            raise PipelineError("Ligand item is missing name/identifier/smiles")
        source = (item.get("source") or "auto").lower()
        identifier = str(item.get("identifier") or "").strip()
        if source == "auto":
            if item.get("smiles"):
                return "smiles", item["smiles"]
            if identifier.isdigit():
                return "pubchem", identifier
            if identifier.upper().startswith("DB") and self.has_drugbank_lookup():
                return "drugbank", identifier.upper()
            if identifier.upper().startswith("CHEMBL"):
                return "chembl", identifier.upper()
            if identifier.upper().startswith("ZINC"):
                return "zinc", identifier.upper()
            if identifier.upper().startswith("MOL"):
                return "tcmsp", identifier.upper()
            if item.get("path"):
                return "local", item["path"]
            if identifier:
                return "pubchem", identifier
            return self.auto_query_ligand_identifier(name)
        if source == "smiles":
            return "smiles", item.get("smiles") or identifier
        if source in {"local", "file"}:
            local_path = Path(item.get("path") or identifier).expanduser()
            if not local_path.is_absolute():
                local_path = self.manifest_path.parent / local_path
            return "local", str(local_path)
        if source in {"pubchem", "chembl", "zinc", "tcmsp", "drugbank"}:
            if identifier:
                return source, identifier
            return self.auto_query_ligand_identifier(name, preferred=source)
        raise PipelineError(f"Unsupported ligand source: {source}")

    def resolve_receptor_item(self, item: dict[str, Any]) -> tuple[str, str]:
        name = item.get("name") or item.get("identifier") or item.get("path")
        if not name:
            raise PipelineError("Receptor item is missing name/identifier/path")
        source = (item.get("source") or "auto").lower()
        identifier = str(item.get("identifier") or "").strip()
        if source == "auto":
            if item.get("path"):
                local_path = Path(item["path"]).expanduser()
                if not local_path.is_absolute():
                    local_path = self.manifest_path.parent / local_path
                return "local", str(local_path)
            if identifier and re.fullmatch(r"[A-Za-z0-9]{4}", identifier):
                return "pdb", identifier.upper()
            if identifier and re.fullmatch(r"[A-NR-Z][0-9][A-Z0-9]{3}[0-9]", identifier.upper()):
                return "alphafold", identifier.upper()
            if item.get("sequence"):
                return item.get("sequence_type", "protein").lower(), item["sequence"]
            return self.auto_query_receptor_identifier(name)
        if source in {"local", "file"}:
            local_path = Path(item.get("path") or identifier).expanduser()
            if not local_path.is_absolute():
                local_path = self.manifest_path.parent / local_path
            return "local", str(local_path)
        if source in {"pdb", "alphafold"}:
            return source, identifier or name
        if source in {"peptide", "protein", "protein_sequence", "nucleic", "nucleic_sequence"}:
            return source, item.get("sequence") or identifier
        raise PipelineError(f"Unsupported receptor source: {source}")

    def auto_query_ligand_identifier(self, name: str, preferred: str | None = None) -> tuple[str, str]:
        clean = name.strip()
        if clean.isdigit():
            return "pubchem", clean
        if clean.upper().startswith("DB") and self.has_drugbank_lookup():
            return "drugbank", clean.upper()
        if clean.upper().startswith("CHEMBL"):
            return "chembl", clean.upper()
        if clean.upper().startswith("ZINC"):
            return "zinc", clean.upper()
        if clean.upper().startswith("MOL"):
            return "tcmsp", clean.upper()
        if preferred == "drugbank":
            if not self.has_drugbank_lookup():
                raise PipelineError("DrugBank source requested but no local catalog or online API credentials are configured")
            return "drugbank", clean
        self.require_requests()
        sources = [preferred] if preferred else ["pubchem", "chembl"]
        for source in sources:
            if source == "pubchem":
                cid = self.query_pubchem_cid(clean)
                if cid:
                    return "pubchem", cid
            elif source == "chembl":
                chembl_id = self.query_chembl_id(clean)
                if chembl_id:
                    return "chembl", chembl_id
        if not preferred:
            chembl_id = self.query_chembl_id(clean)
            if chembl_id:
                return "chembl", chembl_id
            if self.has_drugbank_lookup():
                return "drugbank", clean
        raise PipelineError(f"Could not resolve ligand identifier for {name}")

    def auto_query_receptor_identifier(self, name: str) -> tuple[str, str]:
        self.require_requests()
        clean = name.strip()
        if re.fullmatch(r"[A-Za-z0-9]{4}", clean):
            return "pdb", clean.upper()
        if re.fullmatch(r"[A-NR-Z][0-9][A-Z0-9]{3}[0-9]", clean.upper()):
            return "alphafold", clean.upper()
        pdb_id = self.query_rcsb_entry(clean)
        if pdb_id:
            return "pdb", pdb_id
        accession = self.query_uniprot_accession(clean)
        if accession:
            return "alphafold", accession
        raise PipelineError(f"Could not resolve receptor identifier for {name}")

    def http_get(self, url: str, **kwargs: Any) -> requests.Response:
        self.require_requests()
        retries = int(self.settings.get("max_retries", 3))
        timeout = int(self.settings.get("request_timeout", 30))
        delay = int(self.settings.get("retry_delay", 4))
        last_error: Exception | None = None
        for attempt in range(1, retries + 1):
            try:
                response = self.http.get(url, timeout=timeout, **kwargs)
                if response.status_code == 200:
                    return response
                if response.status_code == 404:
                    return response
                log(f"GET {url} returned {response.status_code} on attempt {attempt}/{retries}", "WARNING")
            except Exception as exc:  # pragma: no cover - network dependent
                last_error = exc
                log(f"GET {url} failed on attempt {attempt}/{retries}: {exc}", "WARNING")
            if attempt < retries:
                time.sleep(delay)
        if last_error:
            raise PipelineError(str(last_error))
        raise PipelineError(f"GET failed for {url}")

    def http_post(self, url: str, **kwargs: Any) -> requests.Response:
        self.require_requests()
        retries = int(self.settings.get("max_retries", 3))
        timeout = int(self.settings.get("request_timeout", 30))
        delay = int(self.settings.get("retry_delay", 4))
        last_error: Exception | None = None
        for attempt in range(1, retries + 1):
            try:
                response = self.http.post(url, timeout=timeout, **kwargs)
                if response.status_code == 200:
                    return response
                log(f"POST {url} returned {response.status_code} on attempt {attempt}/{retries}", "WARNING")
            except Exception as exc:  # pragma: no cover - network dependent
                last_error = exc
                log(f"POST {url} failed on attempt {attempt}/{retries}: {exc}", "WARNING")
            if attempt < retries:
                time.sleep(delay)
        if last_error:
            raise PipelineError(str(last_error))
        raise PipelineError(f"POST failed for {url}")

    def query_pubchem_cid(self, name: str) -> Optional[str]:
        url = f"https://pubchem.ncbi.nlm.nih.gov/rest/pug/compound/name/{quote(name)}/cids/JSON"
        response = self.http_get(url)
        if response.status_code != 200:
            return None
        payload = response.json()
        ids = payload.get("IdentifierList", {}).get("CID", [])
        return str(ids[0]) if ids else None

    def query_chembl_id(self, name: str) -> Optional[str]:
        url = "https://www.ebi.ac.uk/chembl/api/data/molecule/search.json"
        response = self.http_get(url, params={"q": name, "limit": 1})
        if response.status_code != 200:
            return None
        molecules = response.json().get("molecules", [])
        if not molecules:
            return None
        return molecules[0].get("molecule_chembl_id")

    def query_rcsb_entry(self, name: str) -> Optional[str]:
        payload = {
            "query": {"type": "terminal", "service": "full_text", "parameters": {"value": name}},
            "return_type": "entry",
            "request_options": {"paginate": {"start": 0, "rows": 1}},
        }
        response = self.http_post("https://search.rcsb.org/rcsbsearch/v2/query", json=payload)
        if response.status_code != 200:
            return None
        results = response.json().get("result_set", [])
        if not results:
            return None
        return results[0].get("identifier")

    def query_uniprot_accession(self, name: str) -> Optional[str]:
        response = self.http_get(
            "https://rest.uniprot.org/uniprotkb/search",
            params={
                "query": f"(gene:{name} OR protein_name:{name}) AND organism_id:{self.settings['organism_id']}",
                "fields": "accession,protein_name,gene_names",
                "format": "json",
                "size": 1,
            },
        )
        if response.status_code != 200:
            return None
        results = response.json().get("results", [])
        if not results:
            return None
        return results[0].get("primaryAccession")

    def validate_content(self, content: str, source: str) -> bool:
        if len(content.strip()) < 40:
            return False
        normalized = source.lower()
        if normalized in {"pubchem", "chembl", "zinc", "smiles", "sdf", "mol2", "tcmsp"}:
            return any(token in content for token in ["$$$$", "M  END", "@<TRIPOS>MOLECULE"])
        return any(token in content for token in ["ATOM", "HETATM", "HEADER", "TITLE", "data_"])

    def download_text(self, url: str, dest: Path, source: str) -> Path:
        response = self.http_get(url)
        if response.status_code != 200:
            raise PipelineError(f"Download failed from {source}: HTTP {response.status_code}")
        content = response.text
        if not self.validate_content(content, source):
            raise PipelineError(f"Downloaded content from {source} did not validate")
        write_text(dest, content)
        return dest

